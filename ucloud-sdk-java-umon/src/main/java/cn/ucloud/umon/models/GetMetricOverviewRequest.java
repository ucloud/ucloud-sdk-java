package cn.ucloud.umon.models;

import cn.ucloud.common.annotation.NotEmpty;
import cn.ucloud.common.annotation.UCloudParam;
import cn.ucloud.common.request.Request;

import java.util.List;

public class GetMetricOverviewRequest extends Request {

    /** 地域。 参见 [地域和可用区列表](https://docs.ucloud.cn/api/summary/regionlist) */
    @NotEmpty
    @UCloudParam("Region")
    private String region;

    /** 可用区。参见 [可用区列表](https://docs.ucloud.cn/api/summary/regionlist) */
    @NotEmpty
    @UCloudParam("Zone")
    private String zone;

    /** 项目ID，不填为默认项目。子账户必须填写项目ID */
    @UCloudParam("ProjectId")
    private String projectId;

    /** 资源类型 */
    @NotEmpty
    @UCloudParam("ResourceType")
    private String resourceType;

    /** 监控指标名称，若省略则返回所有监控指标数据 */
    @UCloudParam("MetricName")
    private List<String> metricName;

    /** 数据分页值，默认为20 */
    @UCloudParam("Limit")
    private Integer limit;

    /** 数据偏移量，默认为0 */
    @UCloudParam("Offset")
    private Integer offset;

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getZone() {
        return zone;
    }

    public void setZone(String zone) {
        this.zone = zone;
    }

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String getResourceType() {
        return resourceType;
    }

    public void setResourceType(String resourceType) {
        this.resourceType = resourceType;
    }

    public List<String> getMetricName() {
        return metricName;
    }

    public void setMetricName(List<String> metricName) {
        this.metricName = metricName;
    }

    public Integer getLimit() {
        return limit;
    }

    public void setLimit(Integer limit) {
        this.limit = limit;
    }

    public Integer getOffset() {
        return offset;
    }

    public void setOffset(Integer offset) {
        this.offset = offset;
    }
}
